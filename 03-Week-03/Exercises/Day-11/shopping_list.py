## shopping list program

# creating our shopping list

shopping_list = [
    "Milk",
    "Bread",
    "Eggs",
    "Cheese"
]

# disply the entire shopping list
print("Shopping List")
# use our for loop to print each item is the shopping list
for item in shopping_list:
    print(item)

# print the total items on the shopping list by 
# the use of len() function
print("Total Items:", len(shopping_list))



